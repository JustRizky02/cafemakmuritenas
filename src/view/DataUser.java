    public boolean TambahData(){
         boolean isAdded = false;
        //id_user = Integer.parseInt(jTextField2.getText());
        nama = jTextField1.getText();
        password = jTextField3.getText(); 
        telp = Integer.parseInt(jTextField4.getText());
        role = jComboBox1.getSelectedItem().toString();

        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/uas_oop","root","");
            String sql = "INSERT INTO karyawan ( nama, password, telp, role) VALUES ( ?, ?, ?, ?);";
            PreparedStatement rs = cn.prepareStatement(sql);
            //rs.setInt(1, id_user);
            rs.setString(1, nama);
            rs.setString(2, password);
            rs.setInt(3, telp);
            rs.setString(4, role);

            int rowCount = rs.executeUpdate();
            if (rowCount > 0) {
                isAdded = true;
            } else {
                isAdded = false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return isAdded;
    }
