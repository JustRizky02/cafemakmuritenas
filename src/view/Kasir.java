    private void BayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BayarActionPerformed
        double subtotal = 0;
        String[] items = Textarea.getText().split("\n");
        for (int i = 0; i < items.length; i++) {
            String[] item = items[i].split("\t");
            String[] itemTotal = item[3].split(": Rp. ");
            subtotal += Double.parseDouble(itemTotal[1]);
        }
        Pembayaran pembayaranForm = new Pembayaran(subtotal);
        pembayaranForm.setVisible(true);
        //this.dispose();
        
    }//GEN-LAST:event_BayarActionPerformed

