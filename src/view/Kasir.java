    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int i = jTable1.getSelectedRow();
        if (i >= 0) {
        Id.setText(model.getValueAt(i, 0).toString());
        Nama.setText(model.getValueAt(i, 1).toString());
        Harga.setText(model.getValueAt(i, 3).toString());
        Kategori.setText(model.getValueAt(i, 2).toString());

        ImageIcon imageIcon = (ImageIcon) model.getValueAt(i, 4);
        Image image = imageIcon.getImage();

        // Resize gambar menjadi ukuran 325x244
        Image resizedImage = image.getScaledInstance(325, 244, Image.SCALE_SMOOTH);

        // Buat ImageIcon baru dari gambar yang sudah diresize
        ImageIcon resizedImageIcon = new ImageIcon(resizedImage);

        gambar.setIcon(resizedImageIcon);
    }
    }//GEN-LAST:event_jTable1MouseClicked

    private void PesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PesanActionPerformed
            String qtyText = Qty.getText();
            int qty;
            if (qtyText.isEmpty()) {
                qty = 1; // Jika kolom Qty kosong, beri nilai 1
            } else {
                qty = Integer.parseInt(qtyText);
            }
            int id = Integer.parseInt(Id.getText());
            String nama = Nama.getText();
            String kategori = Kategori.getText();
            int harga = Integer.parseInt(Harga.getText());
            double total = harga * qty;

            String item = nama + "\tRp. " + harga + "\t x " + qty + " pcs" + "\t total : Rp. " + total;

            Textarea.append(item + "\n");
            Id.setText("");
            Nama.setText("");
            Kategori.setText("");
            Harga.setText("");
            Qty.setText("");

    }//GEN-LAST:event_PesanActionPerformed

