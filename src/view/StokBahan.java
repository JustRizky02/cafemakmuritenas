    public boolean TambahData(){
         boolean isAdded = false;
        //id_user = Integer.parseInt(jTextField2.getText());
        nama = Nama.getText();
        brand = Brand.getText(); 
        kategori = jComboBox1.getSelectedItem().toString();
        harga = Integer.parseInt(Harga.getText());

        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/uas_oop","root","");
            String sql = "INSERT INTO stok ( nama, brand, kategori, harga) VALUES ( ?, ?, ?, ?);";
            PreparedStatement rs = cn.prepareStatement(sql);
            //rs.setInt(1, id_user);
            rs.setString(1, nama);
            rs.setString(2, brand);
            rs.setString(3, kategori);
            rs.setInt(4, harga);

            int rowCount = rs.executeUpdate();
            if (rowCount > 0) {
                isAdded = true;
            } else {
                isAdded = false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return isAdded;
    }

