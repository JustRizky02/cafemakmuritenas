    public void tampilkan() throws IllegalAccessException{
       String driver = "com.mysql.jdbc.Driver";
       String url = "jdbc:mysql://localhost:3306/uas_oop";  // myDB --> nama database 
       String username = "root";       // user name DMBS
       String password = "";
       int row = jTable1.getRowCount();
       for(int a=0; a<row;a++){
           model.removeRow(0);
       }
        try {
            Class.forName( driver ).newInstance();
            Connection cn = DriverManager.getConnection( url, username, password );
            ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM transaksi"); //command untuk mengambil seluruh isi tabel mahasiswa
            while(rs.next()){
                String data [] = {rs.getString(1),rs.getString(2),rs.getString(3)};
                model.addRow(data);
        }
        } catch (SQLException ex) {
            Logger.getLogger(DataUser.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DataUser.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InstantiationException ex) {
            Logger.getLogger(DataUser.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
    }

